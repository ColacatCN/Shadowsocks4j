package com.life4ever.shadowsocks4j.proxy.exception;

public class Shadowsocks4jProxyException extends Exception {

    public Shadowsocks4jProxyException(String message) {
        super(message);
    }

    public Shadowsocks4jProxyException(String message, Throwable cause) {
        super(message, cause);
    }

    public Shadowsocks4jProxyException(Throwable cause) {
        super(cause);
    }

}
